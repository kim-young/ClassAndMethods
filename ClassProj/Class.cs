using System;
namespace ClassProj
{
    public class Class
    {
        public Class()
        {
        }

		public static int Addition(int number)
		{
			int result = number + number;
			return result;
		}

        public static int Subtract(int number)
		{
			int result = number - number;
			return result;
		}

        public static int Multiply(int number)
		{
			int result = number * number;
			return result;
		}
    }
}
